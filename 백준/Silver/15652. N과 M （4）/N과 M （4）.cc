#include <iostream>
using namespace std;

int arr[10];
bool isUsed[10];

int n, m;

void func(int num, int cnt)
{
    if (cnt == m)
    {
        for (int i = 0; i < m; i++)
        {
            cout << arr[i] << ' ';
        }
        cout << endl;
        return;
    }

    for (int i = num; i <= n; i++)
    {
        if (!isUsed[i])
        {
            arr[cnt] = i;
            func(i, cnt + 1);
        }
    }
}

int main()
{
    cin >> n >> m;
    func(1, 0);
}